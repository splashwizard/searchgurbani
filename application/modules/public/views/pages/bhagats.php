            <p>
                The Bhagats whose Bani has been included in Guru Granth Sahib belong to the period stretching from twelfth century CE to the seventeenth century. After reading and analysing the Bani of these Bhagats as recorded in Guru Granth Sahib, it becomes amply clear that all of them were the worshippers and votaries of One God. They preached the Oneness of God and exhorted the people to worship Him alone. Initially some Bhagats believed in idol worship and multiple gods but by the time they orated the bani that is incorporated in Guru Granth Sahib, they had realized the oneness of God. Very little is known about the lives of some of these Bhagats. <strong>Sikh Bhagats</strong> were holy men of various various sects whose teachings are included in the Sikh holy book the Sri Guru Granth Sahib. The word "bhagat" means devotee, and comes from the Sanskrit word Bhakti, which means devotion and love. There are 15 Bhagats who are given respect in the Guru Granth Sahib as the Bani of the Ten Sikh Gurus. They evolved a belief in one God that preceded Guru Nanak. Guru Arjan Dev selected the writings of The Great Hindu Bhaktis and Sufi saints.
            </p>
