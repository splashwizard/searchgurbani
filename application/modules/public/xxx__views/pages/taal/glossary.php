                        <tr>
                            <td valign="top" bgcolor="#eaeaea" class="newshead2">Dayan</td>
                            <td class="blue">Sometimes called <a href="#d" class="links">dahina</a>. The right hand drum
                                of a <a href="#t" class="links">tabla</a> set usually measuring 4-6 inches in diameter
                                and made of wooden frame. The treble sound of the tabla normally played with the right
                                hand producing the tabla <a href="#b" class="links">bols</a> or notes <i>Na, Ta, Tin,
                                    Tee, Te, Tu, Tet, Tak, Trak, Din</i> and others.
                            </td>
                        </tr>
                        <tr>
                            <td valign="top" bgcolor="#eaeaea" class="newshead2">Dhaivat</td>
                            <td class="blue">See <a href="#s" class="links">saptak</a>.</td>
                        </tr>
                        <tr>
                            <td valign="top" bgcolor="#eaeaea" class="newshead2">Dhrupad</td>
                            <td class="blue">Considered to be the oldest genre of singing it covers themes such as
                                philosophy, devotion and celebration of the seasons. It descended from spiritual music
                                called <a href="#p" class="links">praband</a>, a style which existed around the
                                thirteenth century. Today&rsquo;s style of dhrupad came about in the courts around the
                                fifteenth century. There are now few specialist <i>dhrupad</i> singers for various
                                reasons including lack of popularity. The piece begins with an extended <a href="#a"
                                                                                                           class="links">alap</a>
                                section which gradually increases in speed and uses the <a href="#b"
                                                                                           class="links">bols</a> <i>na,
                                    ne, ri,re, di,de</i>. The composition (itself called <i>dhrupad</i>) follows and is
                                normally played in chau <a href="#t" class="links">tal</a>, a cycle of 12 beats, by the
                                <a href="#p" class="links">pakhavaj</a> player. The <i>dhamar</i> variation of <i>dhrupad</i>
                                is played in 14 beats or dhamar <a href="#t" class="links">tal</a>. Most exponents can
                                be linked in some way to the Dagar family and their disciples. Recordings of Zahiruddin
                                and Wasifuddin Dagar are available as are those of the Gundecha Brothers.
                            </td>
                        </tr>
                        <tr>
                            <td valign="top" bgcolor="#eaeaea" class="newshead2">Dhun</td>
                            <td class="blue">A light classical piece normally played in instrumental form based on a
                                folk melody. They are most commonly played in 8 <a href="#m" class="links">matra</a>
                                (beat) cycle or <i>tal keherva</i>.
                            </td>
                        </tr>
                        <tr>
                            <td valign="top" bgcolor="#eaeaea" class="newshead2">Drut</td>
                            <td class="blue">See <a href="#l" class="links">laya</a>.</td>
                        </tr>
