                        <tr>
                            <td valign="top" bgcolor="#eaeaea" class="newshead2">Kaida</td>
                            <td class="blue">Rule or law. Integral to learning <a href="#t" class="links">tabla</a> a
                                <i>kaida</i> is a theme or an idea which could use quite simple notes but which is then
                                expanded using the specific techniques of the style of <a href="#t"
                                                                                          class="links">tabla</a> being
                                learnt. The expansions or variations of the <i>kaida</i> are referred to as <a href="#p"
                                                                                                               class="links">paltas</a>.
                                It is an improvisational part of a tabla solo performance at an advanced level but can
                                also be used as a tabla exercise at a simple one.
                            </td>
                        </tr>
                        <tr>
                            <td valign="top" bgcolor="#eaeaea" class="newshead2">Kathak</td>
                            <td class="blue">A style of north Indian dancing which traditionally uses the <a href="#p"
                                                                                                             class="links">pakhawaj</a>
                                for accompaniment.
                            </td>
                        </tr>
                        <tr>
                            <td valign="top" bgcolor="#eaeaea" class="newshead2">Kawali</td>
                            <td class="blue">Sometimes written as 'qawwali'. Literally means utterance. It is sung in a
                                variety of rhythms and in this style the lead vocalist will sing a line which is then
                                repeated by several stage singers. The base rhythm is clapped throughout rendition. It's
                                origins can be traced to the tenth century in Persia and <i>kawali</i> in its present
                                form started at the end of the thirteenth century in the Indian sub-continent developed
                                by Amir Khusro. It is considered the devotional music of the Sufis, a mystical sect of
                                the Islamic faith with emphasis placed on inner experience and attaining enlightenment.
                                The most famous <i>kawali</i> singer is of course the late Ustad Nusrat Fateh Ali Khan.
                                Current exponents of this style are Begum Abida Parveen and Sabri Brothers.
                            </td>
                        </tr>
                        <tr>
                            <td valign="top" bgcolor="#eaeaea" class="newshead2">Khali</td>
                            <td class="blue">Literally meaning empty it is a beat or <a href="#m"
                                                                                        class="links">matra</a> played
                                with a dampened or closed sound. The importance of the <i>khali</i> is to make the
                                vocalist or instrumentalist aware of the imminent <a href="#s" class="links">sum</a> or
                                to help to ascertain where he is in a long <a href="#t" class="links">tal</a> or beat
                                cycle. It is denoted by a '0' clearly marked on top of the beat or <a href="#m"
                                                                                                      class="links">matra</a>.
                                There can only ever be one <i>khali</i> in a given <a href="#t" class="links">tal</a>.
                            </td>
                        </tr>
                        <tr>
                            <td valign="top" bgcolor="#eaeaea" class="newshead2">Khayal</td>
                            <td class="blue">Thought, idea, conception, imagination, lyric. Historically said to be a
                                mixture of <a href="#k" class="links">kawali</a> and <a href="#d"
                                                                                        class="links">dhrupad</a>
                                styles. Compositions of <i>khayal</i> are now the most common form of presentation of
                                classical Hindustani music covering aspects such as love, separation, the seasons and
                                praise of various gods and goddesses. A performance normally consists of <i>bada</i> or
                                main <i>khayal</i> sung in slow speed followed by <i>chota</i> or short <i>khayal</i>
                                sung in faster tempo. <i>Khayal</i> has two sections called <a href="#s" class="links">stai</a>
                                and <a href="#a" class="links">antara</a> and the presentation of these parts depends on
                                the <a href="#g" class="links">gharana</a> or style of the artist.
                            </td>
                        </tr>
                        <tr>
                            <td valign="top" bgcolor="#eaeaea" class="newshead2">Komal</td>
                            <td class="blue">A flat of an equivalent note or <a href="#s" class="links">swar</a>. In
                                Indian classical music the notes Re, Ga, Dha and Ni possess flat notes in any given <a
                                    href="#s" class="links">saptak</a> or scale. <i>Komal</i> notes are denoted by a
                                line under the note.
                            </td>
                        </tr>
