                        <tr>
                            <td valign="top" bgcolor="#eaeaea" class="newshead2">Laggi</td>
                            <td class="blue">A fast rendition of certain pre-composed note combinations on <a href="#t"
                                                                                                              class="links">tabla</a>
                                in a particular <a href="#t" class="links">tal</a> to add dynamism and effect to
                                accompaniment. <i>Laggi</i> is normally played towards the end of a <a href="#t"
                                                                                                       class="links">thumri</a>
                                performance and rendered frequently in the accompaniment of <a href="#g" class="links">ghazal</a>.
                            </td>
                        </tr>
                        <tr>
                            <td valign="top" bgcolor="#eaeaea" class="newshead2">Laya</td>
                            <td class="blue">Means the tempo or speed of the <a href="#t" class="links">tal</a>. Three
                                well-known general speeds are referred to as <a href="#v" class="links">vilambit</a> or
                                slow, <a href="#m" class="links">madya</a> or medium and <a href="#d"
                                                                                            class="links">drut</a> or
                                fast. The basic tempo is referred to as <i><i>barabar</i> or thah laya</i> which
                                normally means the original speed or one note per beat (one <a href="#b" class="links">bol</a>
                                per <a href="#m" class="links">matra</a>). A doubling of <i><i>barabar</i></i> would be
                                referred to as <i>duggun</i>. It is not in itself an exact timing but it would be
                                mathematically a doubling of <i>barabar</i> speed. So if <i>barabar</i> was one note per
                                beat then <i>duggun</i> would be two notes per beat. <i>Laya</i> can also sometimes
                                refer to the emphasis placed on certain notes in any given time cycle.
                            </td>
                        </tr>
                        <tr>
                            <td valign="top" bgcolor="#eaeaea" class="newshead2">Layakari</td>
                            <td class="blue">Improvisation of words in a <a href="#k" class="links">khayal</a> piece
                                designed to show the <i>thayari</i> (literally readiness or virtuosity) of the
                                performer.
                            </td>
                        </tr>
                        <tr>
                            <td valign="top" bgcolor="#eaeaea" class="newshead2">Lehera</td>
                            <td class="blue">A simple melody set to a specific number of beats that repeats itself for
                                the purpose of holding a rhythm for a <a href="#t" class="links">tabla</a> solo
                                performance. It can be played on sarangi but normally on harmonium.
                            </td>
                        </tr>
