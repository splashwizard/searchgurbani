                            <td valign="top" bgcolor="#eaeaea" class="newshead2">Saptak</td>
                            <td class="blue">Sapt literally means seven. It is quite simply the Indian classical music
                                scale. It refers to the entire set of seven <a href="#s" class="links">swars</a> or
                                notes of the Indian classical scale; <a href="#s" class="links">shadaj</a> (Sa), <a
                                    href="#r" class="links">rishab</a> (Re), <a href="#g" class="links">gandhar</a>
                                (Ga), <a href="#m" class="links">madyam</a> (Ma), <a href="#p" class="links">pancham</a>
                                (Pa), <a href="#d" class="links">dhaivat</a> (Dha), <a href="#n"
                                                                                       class="links">nishaad</a> (Ni).
                                There are mainly three such scales or <i>saptak</i>; <a href="#m"
                                                                                        class="links">mandra</a> refers
                                to the lower scale and notes are written with dots underneath them, <a href="#m"
                                                                                                       class="links">madya</a>
                                to the middle scale and <a href="#t" class="links">tar</a> to the higher scale where
                                notes are written with dots above them. It is similar to the Western octave although the
                                eighth note is not counted since it is in the same pitch as the starting note. In
                                actuality there are twelve notes in a given scale since Re, Ga, Dha, and Ni have
                                equivalent flat or <a href="#k" class="links">komal</a> notes which are written with a
                                line underneath them and Ma has an equivalent sharp or <a href="#t"
                                                                                          class="links">tivra</a> which
