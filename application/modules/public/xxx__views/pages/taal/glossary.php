                        </tr>
                        <tr>
                            <td valign="top" bgcolor="#eaeaea" class="newshead2">Gat</td>
                            <td class="blue">An improvisational instrumental composition. Also a <a href="#t"
                                                                                                    class="links">tabla</a>
                                composition made up of certain <a href="#b" class="links">bols</a> specific to the
                                learning style that allows room for improvisation and normally follows a <a href="#k"
                                                                                                            class="links">kaida</a>
                                in a <a href="#t" class="links">tabla</a> solo performance.
                            </td>
                        </tr>
                        <tr>
                            <td valign="top" bgcolor="#eaeaea" class="newshead2">Gayaki</td>
                            <td class="blue">A style of singing.</td>
                        </tr>
                        <tr>
                            <td valign="top" bgcolor="#eaeaea" class="newshead2">Gharana</td>
                            <td class="blue">Household or Family. Used to refer to a particular style where the basis
                                would have been the rigid <a href="#g" class="links">guru-shishya parampara</a> or
                                teacher-disciple learning system. The academic Deshpande suggests that a school could
                                only be called a <i>gharana</i> if it were in existence for at least three generations
                                and had markedly different stylistic features from other schools.
                            </td>
                        </tr>
                        <tr>
                            <td valign="top" bgcolor="#eaeaea" class="newshead2">Ghazal</td>
                            <td class="blue">From the Arabic literally meaning talking to women. It is said to have
                                originated in tenth century Iran from the Persian <i>qasida</i>. The part of the qasida
                                called <i>tashbib</i> got detached over years and formed the <i>ghazal</i>. The <i>ghazal</i>,
                                written in Urdu, now rarely exceeds twelve couplets and always opens with a rhyme called
                                <i>matla</i>. Each couplet is self-sufficient, detachable and quotable. The central
                                concern of the <i>ghazal</i> is love but political, social and moral themes are also
                                found. Arguably the most famous writer of <i>ghazal</i> was Ghalib (1797-1869). There
                                are many renown musical exponents of today and yesteryear such as Talat Mahmood, Mehdi
                                Hassan, Ghulam Ali, Hariharan and Jagjit Singh.
                            </td>
                        </tr>
                        <tr>
                            <td valign="top" bgcolor="#eaeaea" class="newshead2">Guru-Shishya Parampara</td>
                            <td class="blue">The traditional teacher-disciple method of learning all forms of Indian
                                classical music. The teacher would impart knowledge on the student that he had gained
                                from his teacher thus maintaining a lineage of artists and protection of the musical
                                material upon which the style was based. This method required of the student almost
                                total dedication to the teacher's art and learning in this format would normally be in a
                                particular style or <a href="#g" class="links">gharana</a>.
                            </td>
                        </tr>
                    </table>
