                            <td valign="top" bgcolor="#eaeaea" class="newshead2">Tabla</td>
                            <td class="blue">From the Arabic 'tabl' meaning drum. The traditional percussion instrument
                                of north India it is played on two separate drums referred to as <a href="#b"
                                                                                                    class="links">bayan</a>
                                (left) or duggi, made of a metal alloy frame and <a href="#d" class="links">dayan</a>
                                (right), made of a wooden frame. Both are covered by goatskin. It is said to be no more
                                than 300 years old and takes most of it's repertoire from the older <a href="#p"
                                                                                                       class="links">pakhawaj</a>.
                                The Delhi <a href="#g" class="links">gharana</a> (style) is considered to be the oldest
                                tabla playing style. Tabla has embedded itself as the foremost Eastern percussion
                                instrument abroad and has been popularised by famous exponents like Ustad Zakir Hussein,
                                Pandit Anindo Chatterjee and more recently in the percussion fusion sounds of Trilok
                                Gurtu.
