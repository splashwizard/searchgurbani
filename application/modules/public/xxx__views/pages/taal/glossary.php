                        <tr>
                            <td valign="top" bgcolor="#eaeaea" class="newshead2">Bada Khayal</td>
                            <td class="blue">See <a href="#k" class="links">khayal</a>.</td>
                        </tr>
                        <tr>
                            <td valign="top" bgcolor="#eaeaea" class="newshead2">Baj</td>
                            <td class="blue">A style or school of playing generally referred to by <a href="#g"
                                                                                                      class="links">gharana</a>.
                            </td>
                        </tr>
                        <tr>
                            <td valign="top" bgcolor="#eaeaea" class="newshead2">Bandish</td>
                            <td class="blue">A composition that consists of words. A <a href="#b"
                                                                                        class="links">bandish</a> is
                                more commonly referred to in classical music to refer to a well-known piece in a
                                particular <a href="#r" class="links">raga</a>. They are taught in order to help the
                                student to understand classical music since it is the <a href="#b"
                                                                                         class="links">bandish</a>
                                itself which characterises many of the elements of the <a href="#r"
                                                                                          class="links">raga</a>.
                            </td>
                        </tr>
                        <tr>
                            <td valign="top" bgcolor="#eaeaea" class="newshead2">Bayan</td>
                            <td class="blue">The left hand drum of a <a href="#t" class="links">tabla</a> set normally
                                measuring 9-12 inches in diameter and made with metal alloy frame such as copper, brass
                                or aluminium. When played it is considered to produce the bass sound of the <a href="#t"
                                                                                                               class="links">tabla</a>
                                and is normally played with the left hand. Amongst others it produces the tabla <a
                                    href="#b" class="links">bols</a> or notes <i>Ghi, Ghe, Ke, Ki Ka and Kat</i>.
                            </td>
                        </tr>
                        <tr>
                            <td valign="top" bgcolor="#eaeaea" class="newshead2">Bhajan</td>
                            <td class="blue">A devotional song sung in light classical style set normally to 6,7 or 8
                                beat cycles. The traditional <i>bhajan</i> has been popularised by many famous saints or
                                gurus of the Eastern sub-continent such as <i>Meera, Tulsidas, Surdas and Krishna</i>.
                                Many mystical bhajans describe the poet&rsquo;s unconditional love for their guru to
                                whom they owe their inner experiences. These bhajans are a speciality of the Gujarat
                                region and are appropriately referred to as <i>sant vani</i> (words of the saint).
                            </td>
                        </tr>
                        <tr>
                            <td valign="top" bgcolor="#eaeaea" class="newshead2">Bol</td>
                            <td class="blue">Words. In musical terms it refers to the words of a composition or <a
                                    href="#t" class="links">tabla</a> notes.
                            </td>
                        </tr>
