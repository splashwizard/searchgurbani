                            <td valign="top" bgcolor="#eaeaea" class="newshead2">Chalan</td>
                            <td class="blue">Movement or flow. The character of a melodic pattern which represents the
                                flow of the <a href="#r" class="links">raga</a> in question.
                            </td>
                        </tr>
                        <tr>
                            <td valign="top" bgcolor="#eaeaea" class="newshead2">Chakradar</td>
                            <td class="blue">A pre-composed <a href="#t" class="links">tabla</a> composition which
                                normally contains an in-built <a href="#t" class="links">tihai</a> but is played three
                                times over and is mathematically calculated to end on the <a href="#s"
                                                                                             class="links">sum</a> of
                                the underlying time-cycle.
                            </td>
                        </tr>
                        <tr>
                            <td valign="top" bgcolor="#eaeaea" class="newshead2">Chota Khayal</td>
                            <td class="blue">See <a href="#k" class="links">khayal</a>.</td>
