                        <tr>
                            <td valign="top" bgcolor="#eaeaea" class="newshead2">Akar</td>
                            <td class="blue">The singing of melody using the vowel sound aa. It can be sung with rhythm
                                as improvisation or without rhythm in <a href="#a" class="links">alap</a> form.
                            </td>
                        </tr>
                        <tr>
                            <td valign="top" bgcolor="#eaeaea" class="newshead2">Alankar</td>
                            <td class="blue">Exercises of specific note combinations which normally possess symmetrical
                                ascent and descent.
                            </td>
                        </tr>
                        <tr>
                            <td valign="top" bgcolor="#eaeaea" class="newshead2">Alap</td>
                            <td class="blue">Introduction of the <a href="#r" class="links">raga</a> without rhythm. It
                                is designed to set the mood of the raga using its common note phrases. Alap can be sung
                                in many styles most notably in <a href="#s" class="links">sargam</a> and <a href="#a"
                                                                                                            class="links">akar</a>.
                            </td>
                        </tr>
                        <tr>
                            <td valign="top" bgcolor="#eaeaea" class="newshead2">Antara</td>
                            <td class="blue">It is the second part of a composition or <a href="#b" class="links">bandish</a>
                                normally consisting of notes in the <a href="#t" class="links">tar saptak</a> (high
                                scale).
                            </td>
                        </tr>
                        <tr>
                            <td valign="top" bgcolor="#eaeaea" class="newshead2">Aroh</td>
                            <td class="blue">The ascent of notes in a <a href="#r" class="links">raga</a>.</td>
                        </tr>
                        <tr>
                            <td valign="top" bgcolor="#eaeaea" class="newshead2">Ati</td>
                            <td class="blue">Denotes an extreme. For example ati <a href="#l" class="links">vilambit
                                    laya</a> would be considered to be very slow tempo.
                            </td>
                        </tr>
                        <tr>
                            <td valign="top" bgcolor="#eaeaea" class="newshead2">Avroh</td>
                            <td class="blue">The descent of notes in a <a href="#r" class="links">raga</a>.</td>
                        </tr>
                        <tr>
                            <td valign="top" bgcolor="#eaeaea" class="newshead2">Avartan</td>
                            <td class="blue">The completion of one cycle of a <a href="#t" class="links">tal</a>.</td>
                        </tr>
